    /**
     * 驼峰命名法工具
     */
    public static String toCamelCase(String s) {
        if (s == null) {
            return null;
        }

        s = s.toLowerCase();

        StringBuilder sb = new StringBuilder(s.length());
        boolean upperCase = false;
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);

            if (c == SEPARATOR) {
                upperCase = true;
            } else if (upperCase) {
                sb.append(Character.toUpperCase(c));
                upperCase = false;
            } else {
                sb.append(c);
            }
        }

        return sb.toString();
    }

    /**
     * 驼峰命名法工具
     */
    public static String toCapitalizeCamelCase(String s) {
        if (s == null) {
            return null;
        }
        s = toCamelCase(s);
        return s.substring(0, 1).toUpperCase() + s.substring(1);
    }

    /**
     * 驼峰命名法工具
     */
    public static String toUnderScoreCase(String s) {
        if (s == null) {
            return null;
        }

        StringBuilder sb = new StringBuilder();
        boolean upperCase = false;
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);

            boolean nextUpperCase = true;

            if (i < (s.length() - 1)) {
                nextUpperCase = Character.isUpperCase(s.charAt(i + 1));
            }

            if ((i > 0) && Character.isUpperCase(c)) {
                if (!upperCase || !nextUpperCase) {
                    sb.append(SEPARATOR);
                }
                upperCase = true;
            } else {
                upperCase = false;
            }

            sb.append(Character.toLowerCase(c));
        }

        return sb.toString();
    }

    /**
     * 驼峰转下划线 (暂不建议使用toUnderScoreCase)
     */
    public static String toUnderscoreName(String camelCaseName) {
        StringBuilder result = new StringBuilder();
        if (camelCaseName != null && camelCaseName.length() > 0) {
            result.append(camelCaseName.substring(0, 1).toLowerCase());
            for (int i = 1; i < camelCaseName.length(); i++) {
                char ch = camelCaseName.charAt(i);
                if (Character.isUpperCase(ch)) {
                    result.append("_");
                    result.append(Character.toLowerCase(ch));
                } else {
                    result.append(ch);
                }
            }
        }
        return result.toString();
    }

    /**
     *
     *	version:将字符串首字母大写
     *	@param strVal
     *	@return
     *----------------------
     * 	author:xiezhyan
     *	date:2017-6-6
     */
    public static String firstUpperCase(String strVal) {
        StringBuffer sb = new StringBuffer();
        if(null != strVal && !"".equals(strVal)) {
            sb.append(String.valueOf(strVal.charAt(0)).toUpperCase());
            for(int i = 1; i < strVal.length(); i++) {
                sb.append(strVal.charAt(i));
            }
        }
        return sb.toString();
    }

    /**
     * 转换为utf-8字符串
     *
     * @param s
     * @return
     */
    public static String toUtf8String(String s) {
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c >= 0 && c <= 255) {
                sb.append(c);
            } else {
                byte[] b;
                try {
                    b = Character.toString(c).getBytes("utf-8");
                } catch (Exception ex) {
                    b = new byte[0];
                }
                for (int j = 0; j < b.length; j++) {
                    int k = b[j];
                    if (k < 0)
                        k += 256;
                    sb.append("%" + Integer.toHexString(k).toUpperCase());
                }
            }
        }
        return sb.toString();
    }

    /**
     * version:得到UUID
     */
    public static String uuid() {
        return UUID.randomUUID().toString().toUpperCase().replace("-", "");
    }


    /**
     * 判断是否不为空
     */
    public static boolean isEmpty(String token) {
        return isBlank(token);
    }

    public static int getHash(String str) {
        int p = 16777619;
        int hash = -2128831035;
        for (int i = 0; i < str.length(); i++) {
            hash = (hash ^ str.charAt(i)) * 16777619;
        }
        hash += (hash << 13);
        hash ^= hash >> 7;
        hash += (hash << 3);
        hash ^= hash >> 17;
        hash += (hash << 5);
        if (hash < 0) {
            hash = Math.abs(hash);
        }
        return hash;
    }
